#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	size_t	r;
	int		s;

	i = 0;
	r = 0;
	s = 1;
	while (str[i] && ((str[i] >= 9 && str[i] <= 13) || str[i] == 32))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + (str[i] - '0');
		i++;
	}
	if (r > 2147483647 && s == 1)
		return (0);
	else if (r > 2147483648 && s == -1)
		return (0);
	return (r * s);
}